    private void tambahData() {
        try {
            String nama = txtNama.getText();
            double jarak = Double.parseDouble(txtHarga.getText());
            int kepadatan = Integer.parseInt(txtIsi.getText());
            long harga = Long.parseLong(txtProtein.getText());
            int aksesibilitas = Integer.parseInt(txtGula.getText());
            int luas = Integer.parseInt(txtKalori.getText());
            String info = txtInfo.getText();

            Connection c = Koneksi.konekKeDB();
            String query = "INSERT INTO tempat_usaha (nama_tempat, jarak, kepadatan, harga, aksesibilitas, luas, info) VALUES (?, ?, ?, ?, ?, ?, ?)";

            PreparedStatement pst = c.prepareStatement(query);
            pst.setString(1, nama);
            pst.setDouble(2, jarak);
            pst.setInt(3, kepadatan);
            pst.setLong(4, harga);
            pst.setInt(5, aksesibilitas);
            pst.setInt(6, luas);
            pst.setString(7, info);

            pst.executeUpdate();

            pst.close();
            c.close();

            SPK.loadAlternatif();

            JOptionPane.showMessageDialog(null, "Berhasil Menambahkan Data Baru");
            dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Pastikan input jarak, kepadatan, harga, aksesibilitas, dan luas adalah angka yang valid.");
        }
    }
